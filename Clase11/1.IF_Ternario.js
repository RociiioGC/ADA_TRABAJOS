// Ejemplo verificar si una persona es mayor de edad
// Paso 1: Declaramos la variable de la edad de la persona

let edad = 18;

// Paso 2: Usamos el operador ternario para determinar si es mayor de edad 

let mensaje = edad >= 20 ? "Mayor de edad" : "Menor de edad";
// condicion: Es mayor de 18?
// si es verdadero el resultado sera  "Mayor de edad" 
// si es falso el resultado sera  "Manor de edad" 

// Paso 3: mostrar por consola:

console.log(mensaje);

// Estructura: condicion ? "valor si es verdadero" : "valor si es falso";
